    private void PronadjiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PronadjiActionPerformed
       
        List<Uposlenik> uposlenici=null;
        
        try{
        
        String imeTextBox=imeTxt.getText();
        
        if(imeTextBox !=null &&imeTextBox.trim().length()>0){
            uposlenici=Baza.nadjiUposlenog(imeTextBox);
        }
        else
            uposlenici=Baza.sviUposlenici();
        
        ModelTabelaUposlenika Model=new ModelTabelaUposlenika(uposlenici);
        tableUposlenici.setModel(Model);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(tableUposlenici, e.getCause());
        }
    }//GEN-LAST:event_PronadjiActionPerformed

