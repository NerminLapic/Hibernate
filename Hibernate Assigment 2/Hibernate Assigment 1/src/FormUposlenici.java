    private void btnIzmjeniUposlenikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmjeniUposlenikaActionPerformed
       
        int red=tableUposlenici.getSelectedRow();
        FormUposlenici formUposlenici = null;
        
        if(red<0){
            JOptionPane.showMessageDialog(FormUposlenici.this, "Morate selektovati uposlenika!!!");
            return;
        }
        
    
        Uposlenik tempUposlenik=ModelTabelaUposlenika.uposlenici.get(tableUposlenici.convertRowIndexToModel(red));
     
        IzmjeniUposlenika izmjeniUposlenika=new IzmjeniUposlenika(formUposlenici,true,tempUposlenik);
        
        id=tempUposlenik.getId();
        izmjeniUposlenika.setVisible(true);
        
        


     

        
        
        
        
    }//GEN-LAST:event_btnIzmjeniUposlenikaActionPerformed

    private void btnObrisiUposlenikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiUposlenikaActionPerformed
        
        int red=tableUposlenici.getSelectedRow();
        FormUposlenici formUposlenici = null;
        
        if(red<0){
            JOptionPane.showMessageDialog(FormUposlenici.this, "Morate selektovati uposlenika!!!");
            return;
        }

        Uposlenik tempUposlenik=ModelTabelaUposlenika.uposlenici.get(tableUposlenici.convertRowIndexToModel(red));
        
        
        int dialogButton=JOptionPane.YES_NO_OPTION;
       int odgovor= JOptionPane.showConfirmDialog(FormUposlenici.this, "Jeste li sigurni da želite obristai selektovanog upolenika?","Upozorenje",dialogButton);
        
        if(odgovor==JOptionPane.YES_OPTION){
            
            
            try {
                Baza.brisiUposlenika(tempUposlenik.getId());
                
            } catch (SQLException ex) {
                Logger.getLogger(FormUposlenici.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else{
           return;
        }
          
       
        
    }//GEN-LAST:event_btnObrisiUposlenikaActionPerformed

